    <h3>Politique de Cookies</h3>
    <p>Dernière mise à jour: 17/03/2025</p>
    <p>Bienvenue sur Sunstar. Cette politique de cookies explique comment nous utilisons les cookies et technologies similaires sur notre site.</p>
    <ol>
        <li><b><u>Qu'est-ce qu'un cookie ?</u></b></li>
            <p>Un cookie est un petit fichier stocké sur votre appareil (ordinateur, tablette, smartphone) lorsque vous visitez un site web. Il permet de conserver des informations sur votre navigation.</p>
        <li><b><u>Types de cookies utilisés</u></b></li>
            <p>Nous utilisons les types de cookies suivants :</p>
            <ul>
                <li><b>Cookies essentiels </b>: nécessaires au bon fonctionnement du site (connexion, sécurité, préférences utilisateur).</li>
                <li><b>Cookies analytiques </b>: nous aident à comprendre comment les utilisateurs naviguent et interagissent avec le site.</li>
                <li><b>Cookies de fonctionnalité </b>: améliorent l’expérience utilisateur (préférences d’affichage, langue, etc.).</li>
                <li><b>Cookies tiers </b>: utilisés par des services externes comme Google Analytics ou réseaux sociaux.</li>
            </ul>
        <li><b><u>Gestion des cookies</u></b></li>
            <p>Vous pouvez configurer votre navigateur pour accepter, refuser ou supprimer les cookies. Voici comment procéder selon votre navigateur :</p>
            <ul>
                <li><b>Google Chrome </b>: <a href="https://support.google.com/chrome/answer/95647?hl=fr" target="blank">lien vers l’aide</a></li>
                <li><b>Mozilla Firefox </b>: <a href="https://support.mozilla.org/fr/kb/activer-desactiver-cookies-preferences" target="blank">lien vers l’aide</a></li>
                <li><b>Safari </b>: <a href="https://support.apple.com/fr-fr/guide/safari/sfri11471/mac" target="blank">lien vers l’aide</a></li>
                <li><b>Microsoft Edge </b>: <a href="https://support.microsoft.com/fr-fr/microsoft-edge/afficher-supprimer-et-g%C3%A9rer-les-cookies-dans-microsoft-edge-63947406-40ac-c3b8-57b9-2a946a29ae09" target="blank">lien vers l’aide</a></li>
                <li><b>Opera </b>: <a href="https://help.opera.com/en/latest/web-preferences/#cookies" target="blank">lien vers l’aide</a></li>
            </ul>
        <li><b><u>Durée de conservation des cookies</u></b></li>
            <p>Les cookies sont stockés pour une durée limitée selon leur type. Certains expirent dès la fermeture du navigateur, d’autres restent actifs plus longtemps.</p>
        <li><b><u>Modification de la politique de cookies</u></b></li>
            <p>Nous pouvons mettre à jour cette politique à tout moment. Les modifications seront publiées sur cette page. <br>Merci d’utiliser Sunstar !</p>
    </ol>
