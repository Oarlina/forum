    <h3>Politique de confidentialité</h3>
    <p>
        Derniere mise a jour : 17/03/2028 <br> Bienvenue sur Sunstar ! La protection de vos données personnelles est une priorité. Cette politique de confidentialité explique comment nous collectons, utilisons et protégeons vos informations.
    </p>
    <ol>
        <li><b><u>Données collectées</u></b></li>
            <p>Nous collectons les informations suivantes :</p>
            <ul>
                <li><b>Informations de compte </b>: nom d'utilisateur, adresse e-mail, mot de passe (chiffré).</li>
                <li><b>Contenu publié </b>: messages, commentaires et toute autre contribution au forum.</li>
                <li><b>Données techniques </b>: adresse IP, type de navigateur, historique de navigation sur le site.</li>
            </ul>
        <li><b><u>Utilisation des données</u></b></li>
            <p>Nous utilisons vos données pour :</p>
            <ul>
                <li>Permettre l'inscription et l'accès au forum.</li>
                <li>Améliorer l'expérience utilisateur et la sécurité.</li>
                <li>Communiquer avec vous en cas de besoin.</li>
                <li>Faire respecter nos conditions d'utilisation.</li>

            </ul>
        <li><b><u>Partage des données</u></b></li>
            <p>Vos données ne sont ni vendues ni partagées avec des tiers, sauf dans les cas suivants :</p>
            <ul>
                <li>Obligations légales (réquisition judiciaire, respect des lois).</li>
                <li>Protection de nos droits et de la sécurité des utilisateurs.</li>
            </ul>
        <li><b><u>Sécurité des données</u></b></li>
            <p>Nous mettons en place des mesures de sécurité pour protéger vos informations contre tout accès non autorisé, modification ou divulgation.</p>
        <li><b><u>Cookies</u></b></li>
            <p>Nous utilisons des cookies pour améliorer votre navigation. Vous pouvez les gérer via les paramètres de votre navigateur.</p>
        <li><b><u>Vos droits</u></b></li>
            <p>Conformément à la réglementation en vigueur, vous avez le droit de :</p>
            <ul>
                <li>Accéder à vos données personnelles.</li>
                <li>Demander leur modification ou suppression.</li>
                <li>Limiter leur traitement.</li>
            </ul>
        <li><b><u>Modification de la politique de confidentialité</u></b></li>
            <p>Nous nous réservons le droit de modifier cette politique. Toute mise à jour sera notifiée sur le forum. <br>Merci d’utiliser Sunstar !</p>
</ol>
